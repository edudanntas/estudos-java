public class Calculadora {
    /**
     * Método que faz a soma de dois numeros
     * @param numeroUm Primeiro parametro do método, ele necessita ser um número inteiro e será somado ao segundo parametro
     * @param numeroDois Segundo parametro do método, ele necessita ser um número inteiro e será somado ao primeoro parametro
     * @return Retorna a soma do parametro um e parametro dois
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}